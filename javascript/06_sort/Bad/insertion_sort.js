let unsorted = [14, -4, 17, 6, 22, 1, -5];

insertionSort(unsorted);

function insertionSort(arr) {
  // ----------------------------------------------------------------
  // 這是一個 for 迴圈，從數組的第二個元素開始迭代（因為第一個元素自己被認為已經排序）。
  // ----------------------------------------------------------------
  for (let j = 1; j <= arr.length - 1; j++) {
    // ----------------------------------------------------------------
    // 在每次迭代中，將當前要處理的元素賦值給變數 key。
    // 定義一個新的變數 i，並將其初始化為當前元素前一個元素的索引。
    // ----------------------------------------------------------------
    let key = arr[j];
    i = j - 1;
    // ----------------------------------------------------------------
    // 進行一個 while 迴圈，如果 i 不小於 0（意味著還沒有遍歷到數組的開頭），
    // 且當前 i 位置的元素大於 key，則繼續迴圈。
    // ----------------------------------------------------------------
    while (i >= 0 && arr[i] > key) {
      // ----------------------------------------------------------------
      // 將 i 位置的元素向右移動一個位置。
      // ----------------------------------------------------------------
      arr[i + 1] = arr[i];
      i -= 1;
    }

    // ----------------------------------------------------------------
    // 將 key 插入到正確的位置。
    // ----------------------------------------------------------------
    arr[i + 1] = key;
  }

  console.log(arr);
  return arr;
}
